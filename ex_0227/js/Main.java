public class Main {
  public static void main(String[] args) {
    System.out.println("Hello World");
    System.out.println("print'i'가 아니라");
    System.out.println("print'l'이였네");
    System.out.println("'아이'가 아니라 '엘'...");
    System.out.println(5 + 5);
    //print구문 안에서 텍스트 작성과 연산을 동시에 하기 위해 괄호를 사용해야 하며 텍스트와 연산 사이 +기호가 필요함.
    System.out.println("텍스트 안에서 연산을 같이 하려면" + (5 + 4) + "라고하면 되나");
  }
}